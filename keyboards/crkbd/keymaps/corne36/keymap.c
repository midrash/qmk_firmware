    switch (keycode) {
            /* KEYBOARD PET STATUS START */
        case OSM(MOD_LCTL):
        case KC_LCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case LT(1, KC_SPC):
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        case TO(0):
            if (record->event.pressed) {
                isWindows  = true;
            }
            break;
        case TO(5):
            if (record->event.pressed) {
                isWindows  = false;
            }
            break;

            /* KEYBOARD PET STATUS END */
    }
    return true;
